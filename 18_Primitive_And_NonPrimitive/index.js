// Primitive Data Types
var person = "Shariar Ahamed";
var newPerson =  person;
person = "Rakib Hassan";

console.log(person); // Rakib Hassan
console.log(newPerson); // Shariar Ahamed




// Non-Primitive Data Types
var person1 = {name: "Shamim Reza"};
var newPerson1 =  person1;
person1.name = "Arif Hossain";

console.log(person1); //{ name: 'Arif Hossain' }
console.log(newPerson1); //{ name: 'Arif Hossain' }
// Subtraction
const money = 500;
const totalExpense = 250;

const result = money - totalExpense;

console.log("Total Result:", result ); // [ 'Total Result:', 250 ]




// Multiplication
const applePrice = 180;
const need = 7;

const totalApplePrice = applePrice * need;

console.log("Total Apple Price:", totalApplePrice); // [ 'Total Apple Price:', 1260 ]




// Division

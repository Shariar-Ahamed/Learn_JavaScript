// Find the length of the array
var number = [50, 80, 77, 45, 90, 100 ];
console.log(number.length); // 6



// Find the index position of an element in an array
var number = [50, 80, 77, 45, 90, 100 ];
console.log(number.indexOf(77)); // 2

var element = number.indexOf(77);
console.log(element); // 2



// Find the value of an element in an array
var number = [50, 80, 77, 45, 90, 100 ];
console.log(number[2]); // 77

var secndElement = number[2];
console.log(secndElement); // 77




// Change the value of an element in an array
var number = [50, 80, 77, 45, 90, 100 ];
console.log(number); // [ 50, 80, 77, 45, 90, 100 ]
number[0] = 33;
console.log(number); // [ 33, 80, 77, 45, 90, 100 ]

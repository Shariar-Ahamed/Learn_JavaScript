// Switch Case Example

let num = 0;

switch (num) {
    case 0:
        console.log("The number is zero"); // 'The Number is zero'
        break;
    case 1:
        console.log("The number is one");
        break;
    case 2:
        console.log("The number is two");
        break;
    default:
        console.log("The number is not zero, one, or two");
}






// While loop 
let num = 0;                    // Loop variable
while ( num <5 ){              // loop condition : যতক্ষণ num < 5, ততক্ষণ loop চলবে
        console.log(num);     // loop statement : বর্তমান num print হবে          
        num++;               // loop veriable change : num বাড়বে ১ করে
}
// [Output: 0,1,2,3,4]


// For loop
for ( let num = 0 ; num <5 ; num++ ) {                  // for ( Loop variable ; loop condition ; loop veriable change )
        console.log(num);       // 0,1,2,3,4           //loop statement
}